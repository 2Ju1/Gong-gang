use DB2024Team05;

# 공대에 사용 가능한 공간들에 대해 저장할 테이블 생성
# 공간 번호, 이름, 위치를 속성으로 가짐
CREATE TABLE DB2024_Space_Info (
                                   Room_Number VARCHAR(50) PRIMARY KEY,
                                   Room_Name VARCHAR(50) not null,
                                   Location VARCHAR(50) not null
);

# 공대에 존재하는 강의실에 대해 저장할 테이블 생성
# 공간 번호, 실습 할 컴퓨터가 있는지, 좌석 수, 예약 필요 여부, 녹화 가능 여부, 녹화하는 카메라의 종류, 수업이 언제 있는지를 속성으로 가짐 
CREATE TABLE DB2024_Classroom (
    Room_Number VARCHAR(10) PRIMARY KEY,
    Practicable VARCHAR(20),
    Seat_Count INT,
    Projector VARCHAR(20),
    Outlet_Count INT,
    Reservation_Required VARCHAR(20),
    Recording_Available VARCHAR(20),
    CameraType VARCHAR(50)
);
# 강의실에 수업이 언제 있는지에 대해 저장할 테이블 생성
# 강의시간, 공간 번호를 속성으로 가짐
CREATE TABLE DB2024_Classroom_Schedule (
    Lecture_Time VARCHAR(10),
    Room_Number VARCHAR(10),
    FOREIGN KEY (Room_Number) REFERENCES DB2024_Space_Info(Room_Number),
    PRIMARY KEY(Room_Number, Lecture_Time)
);



# 강의실 외의 공간에 대해 저장할 테이블 생성
# 공간 번호, 취식 가능 여부, 소음이 발생해도 되는지, 예약 필요 여부, 좌석수, 콘센트 개수를 속성으로 가짐
CREATE TABLE DB2024_Classroom_External (
                                           Room_Number VARCHAR(50) PRIMARY KEY,
                                           Eat_Available BOOLEAN,
                                           Noise_Level BOOLEAN,
                                           Reservation_Needed BOOLEAN,
                                           Seat_Count INT,
                                           Outlet_Count INT,
                                           FOREIGN KEY (Room_Number) REFERENCES DB2024_Space_Info(Room_Number)
);


# 교수님의 정보들에 대해 저장할 테이블 생성
# 교번, 교수님 성함, 연구실 위치, 전화번호, 이메일을 속성으로 가짐
CREATE TABLE DB2024_Professor (
                                  Professor_Num INT,
                                  Name VARCHAR(100),
                                  Lab_Location VARCHAR(100),
                                  Phone VARCHAR(50),
                                  Email VARCHAR(100),
                                  PRIMARY KEY (Professor_Num, Name)
);

# 강의 정보에 대해 저장할 테이블 생성
# 학수번호, 분반 번호, 강의 이름, 교수님 성함, 교번, 강의실 공간 번호, 강의가 진행되는 시간을 속성으로 가진다.
CREATE TABLE DB2024_Lecture (
                                Lecture_Num INT,
                                Class_Num INT,
                                Lecture_Name VARCHAR(100) not null,
                                Professor_Name VARCHAR(100), /*교수님 1명만*/
                                Professor_Num INT,
                                Room_Number VARCHAR(50) not null,
                                Lecture_Time1 VARCHAR(50),
                                Lecture_Time2 VARCHAR(50), /*수업시간 1or2개로*/
                                PRIMARY KEY(Lecture_Num, Class_Num),
                                FOREIGN KEY (Room_Number) REFERENCES DB2024_Space_Info(Room_Number),
                                FOREIGN KEY (Professor_Num, Professor_Name) REFERENCES DB2024_Professor(Professor_Num, Name)
);

/*
DB2024_Lecture 는 DB2024_Space_Info, DB2024_Professor를 외래키로 참조하고
DB2024_Classroom_External와 DB2024_Classroom는 DB2024_Space_Info를 외래키로 참조하므로
이를 고려하여 데이터 입력 순서는 
DB2024_Professor, DB2024_Space_Info, DB2024_Classroom, DB2024_Classroom_External, DB2024_Lecture
이다.
*/

